    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        if(test) {
            int reponse=JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer la chambre");
            if(reponse==0){
                if(chs.delete(chs.findById(id)))
                    JOptionPane.showMessageDialog(this, "chambre bien supprimée");
            }
            teltxt.setText("");
            listeCategories.setSelectedIndex(-1);
            load();
        }
    }//GEN-LAST:event_supprimerActionPerformed

