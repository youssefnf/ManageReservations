    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        if (test) {
            String tel=teltxt.getText();
            Categorie cat = (Categorie) listeCategories.getSelectedItem();
            if (tel.equals(""))
                JOptionPane.showMessageDialog(this, "Il faut remplir le telephone", "WARNING!", JOptionPane.WARNING_MESSAGE);
            else {
                Chambre ch=chs.findById(id);
                ch.setTelephone(tel);
                ch.setCategorie(cat);
                int reponse=JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier la chambre");
                if(reponse==0){
                    if(chs.update(ch))
                        JOptionPane.showMessageDialog(this, "Chambre bien modifiée");
                }
            }
            teltxt.setText("");
            listeCategories.setSelectedIndex(-1);
            load();
        }
    }//GEN-LAST:event_modifierActionPerformed


